    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel m = (DefaultTableModel) SearchTable.getModel();

        int id = -1;
        ArrayList<Student> foundStudents = new ArrayList<>();
        if (!SearchIDtext.getText().isEmpty()) {
            try {
                id = Integer.parseInt(SearchIDtext.getText());
            } catch (NumberFormatException exception) {
                JOptionPane.showMessageDialog(this, "Enter a numeric ID");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Enter a Valid ID");
            }

            Student s = sms.getRecord(id);
            if (s != null) {
                m.addRow(new Object[]{s.getStudentID(), s.getName(), s.getAge(), s.getGender(), s.getDepartment(), s.getGpa()});
            }


        }
           else {
            if(!SearchNameText.getText().isEmpty()){
                foundStudents = sms.getRecordByName(SearchNameText.getText());
                if (!foundStudents.isEmpty()) {
                    for (int i = 0; i < foundStudents.size(); i++) {
                        Student z = foundStudents.get(i);
                        m.addRow(new Object[]{z.getStudentID(), z.getName(), z.getAge(), z.getGender(), z.getDepartment(), z.getGpa()});
                        System.out.println(z.getStudentID() + " " + z.getName());

                    }
                }
            }
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        String name = "", gender = "";
        int age =0;
        double gpa = 0;
        int selectedRow = SearchTable.getSelectedRow();
        
        
            
        

        if (Name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Name");
            return;
        } else {
            name = Name.getText();
        }

        if (Age.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter an Age");
            return;
        }
        try {
            age = Integer.parseInt(Age.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for Age");
            return;
        }

        if (Male.isSelected()) {
            gender = "Male";
        } else if (Female.isSelected()) {
            gender = "Female";
        } else {
            JOptionPane.showMessageDialog(this, "Please select a gender");
            return;
        }

        String dep = (String) Department.getSelectedItem();

        if (Gpa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a GPA");
            return;
        }
        try {
            gpa = Double.parseDouble(Gpa.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for GPA");
            return;
        }

        if (age < 0) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Age");
            return;
        }

        if (gpa > 4 || gpa < 0) {
            JOptionPane.showMessageDialog(this, "Please enter a GPA between 0 and 4");
            return;
        }

        if (!name.matches("[A-Za-z]+")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Name");
            return;
        }
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a student to delete.");
            return;
        }
        
        int id = (int) SearchTable.getValueAt(selectedRow, 0);
        String idString = String.valueOf(id);
        Student s = new Student(id, name, age, gender, dep, gpa);
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to update student with ID: " + id + "?",
                "Confirm update",
                javax.swing.JOptionPane.YES_NO_OPTION
        );
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            sms.update(idString,s);
            javax.swing.JOptionPane.showMessageDialog(this, "Student updated successfully.");
           
        }
        setVisible(false);
    }//GEN-LAST:event_UpdateButtonActionPerformed

